def _save_plot(output_path: Path) -> None:
    """
    Business objective
    ------------------
    Save figures consistently so they can be reused directly in the final
    PowerPoint.

    Coding objective
    ----------------
    Create parent folders, save the plot at presentation quality, and close the
    figure to avoid memory buildup.

    Important implementation note
    -----------------------------
    We intentionally do not call plt.tight_layout() here because several
    presentation figures use fig.text() captions below the axes. Calling
    tight_layout globally can move axes on top of those captions. Individual
    plotting functions control their own spacing with subplots_adjust().
    """

    output_path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(output_path, dpi=180, bbox_inches="tight", pad_inches=0.25)
    plt.close()

def _wrap_text(text: str, width: int = 42) -> str:
    """
    Business objective
    ------------------
    Keep long labels and recommendations readable inside business-facing plots.

    Coding objective
    ----------------
    Wrap long strings into multiple lines for matplotlib annotations.
    """

    return "\n".join(textwrap.wrap(str(text), width=width))


def _classify_feature_group(feature: str) -> str:
    """
    Business objective
    ------------------
    Make feature importance easier to interpret by separating deployable
    operational signals from post-build diagnostic measurements.

    Coding objective
    ----------------
    Return a standardized feature group label used for plotting.
    """

    if feature in POST_BUILD_FEATURES:
        return "Post-build diagnostic"

    if feature in PROCESS_FEATURES:
        return "Process / monitoring"

    if feature in CATEGORICAL_FEATURES:
        return "Material / categorical"

    return "Other"


def _read_challenge_report(
    eda_output_dir: str | Path,
    file_name: str,
) -> pd.DataFrame:
    """
    Business objective
    ------------------
    Reuse the independent challenge reports already generated by run_eda.py
    instead of silently trusting the original EDA conclusions.

    Coding objective
    ----------------
    Read a challenge CSV from reports/eda/challenge and return an empty
    DataFrame if it is not available.
    """

    path = Path(eda_output_dir) / "challenge" / file_name

    if not path.exists():
        return pd.DataFrame()

    return pd.read_csv(path)


def _format_metric(value: object, digits: int = 3) -> str:
    """
    Business objective
    ------------------
    Keep numeric evidence readable in presentation tables.

    Coding objective
    ----------------
    Format floats consistently while leaving non-numeric values unchanged.
    """

    try:
        return f"{float(value):.{digits}f}"
    except (TypeError, ValueError):
        return str(value)


