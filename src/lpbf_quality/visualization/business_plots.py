def plot_quality_class_distribution(
    df: pd.DataFrame,
    output_dir: str | Path = BUSINESS_FIGURES_DIR,
) -> None:
    """
    Business objective
    ------------------
    Show whether the target classes are sufficiently represented for supervised
    learning.

    Coding objective
    ----------------
    Generate a target distribution chart with class percentages and imbalance
    interpretation.
    """

    output_dir = Path(output_dir)

    counts = df[TARGET_COLUMN].value_counts()
    min_count = counts.min()
    max_count = counts.max()
    imbalance_ratio = max_count / min_count if min_count > 0 else float("inf")

    plt.figure(figsize=(10, 5))
    ax = sns.countplot(data=df, x=TARGET_COLUMN, order=QUALITY_ORDER)

    total = len(df)

    for container in ax.containers:
        ax.bar_label(
            container,
            labels=[
                f"{int(v.get_height())}\n{v.get_height() / total:.1%}"
                for v in container
            ],
            fontsize=9,
        )

    ax.text(
        0.03,
        0.92,
        (
            "Key finding\n"
            f"Class imbalance ratio = {imbalance_ratio:.2f}\n"
            "Acceptable for baseline modeling\n"
            "Track recall/F1 by class"
        ),
        transform=ax.transAxes,
        ha="left",
        va="top",
        fontsize=10,
        bbox=dict(boxstyle="round,pad=0.45", facecolor="white", edgecolor="black"),
    )

    plt.title("Quality Class Distribution")
    plt.xlabel("Quality Class")
    plt.ylabel("Number of Samples")

    _save_plot(output_dir / "02_quality_class_distribution.png")


def plot_data_quality_audit(
    eda_output_dir: str | Path,
    output_dir: str | Path = BUSINESS_FIGURES_DIR,
) -> None:
    """
    Business objective
    ------------------
    Summarize missing values, duplicates, schema issues, invalid ranges, and
    constant features in one reviewable quality gate slide.

    Coding objective
    ----------------
    Read validation reports and create a pass/warn/fail summary table.
    """

    eda_output_dir = Path(eda_output_dir)
    output_dir = Path(output_dir)

    def count_status(file_name: str, status: str) -> int:
        path = eda_output_dir / file_name

        if not path.exists():
            return 0

        report = pd.read_csv(path)

        if "status" not in report.columns:
            return 0

        return int((report["status"] == status).sum())

    missing_path = eda_output_dir / "missing_values.csv"
    duplicates_path = eda_output_dir / "duplicates.csv"
    physical_path = eda_output_dir / "physical_ranges.csv"

    missing_count = 0
    if missing_path.exists():
        missing = pd.read_csv(missing_path)
        if "missing_count" in missing.columns:
            missing_count = int(missing["missing_count"].sum())

    duplicate_count = 0
    if duplicates_path.exists():
        duplicates = pd.read_csv(duplicates_path)
        if "duplicate_count" in duplicates.columns:
            duplicate_count = int(duplicates["duplicate_count"].sum())

    invalid_range_count = 0
    if physical_path.exists():
        physical = pd.read_csv(physical_path)
        if "invalid_count" in physical.columns:
            invalid_range_count = int(physical["invalid_count"].sum())

    constant_warnings = count_status("constant_features.csv", "WARN")

    rows = [
        ["Schema validation", "PASS", "Expected columns available"],
        ["Missing values", "PASS" if missing_count == 0 else "WARN", str(missing_count)],
        ["Duplicate rows", "PASS" if duplicate_count == 0 else "WARN", str(duplicate_count)],
        [
            "Invalid physical ranges",
            "PASS" if invalid_range_count == 0 else "WARN",
            str(invalid_range_count),
        ],
        [
            "Constant / near-constant features",
            "PASS" if constant_warnings == 0 else "WARN",
            str(constant_warnings),
        ],
    ]

    fig, ax = plt.subplots(figsize=(12, 5.8))
    ax.axis("off")

    ax.text(
        0,
        1.08,
        "Data Quality Audit",
        fontsize=16,
        fontweight="bold",
        ha="left",
        transform=ax.transAxes,
    )

    ax.text(
        0,
        1.0,
        "Mandatory production checks found no blocking data quality issue.",
        fontsize=10,
        ha="left",
        transform=ax.transAxes,
    )

    table = ax.table(
        cellText=rows,
        colLabels=["Check", "Status", "Details"],
        loc="center",
        cellLoc="left",
        colLoc="left",
    )

    table.auto_set_font_size(False)
    table.set_fontsize(10)
    table.scale(1, 1.5)

    for key, cell in table.get_celld().items():
        cell.set_edgecolor("black")
        if key[0] == 0:
            cell.set_text_props(weight="bold")

    ax.text(
        0,
        -0.08,
        (
            "Engineering interpretation: the dataset is clean enough for modeling, "
            "but production risks still need review."
        ),
        fontsize=10,
        fontweight="bold",
        ha="left",
        transform=ax.transAxes,
    )

    _save_plot(output_dir / "03_data_quality_audit.png")


