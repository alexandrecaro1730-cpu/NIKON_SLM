    # 04-08: data realism and LPBF physics evidence.
    plot_feature_distributions_physics(df, output_dir)
    plot_outlier_assessment(eda_output_dir, output_dir)
    plot_physics_correlation_heatmap(df, output_dir)
    plot_process_window_power_speed(df, output_dir)
    plot_energy_density_quality_physics(df, output_dir)

    # 09: independent challenge layer; this is the key senior-engineering slide.
    plot_physics_consistency_challenge(df, eda_output_dir, output_dir)

    # 10: feature signal and leakage-aware feature interpretation.
    plot_top_mutual_information_features(
        eda_output_dir / "mutual_information.csv",
        output_dir,
    )

    # 11-12: production framing.
    plot_prediction_timing_timeline(output_dir)
    plot_prediction_scenarios_table(output_dir)

    # 13: operational risks and mitigations.
    plot_risk_assessment_details(
        eda_output_dir / "production_risk_assessment.csv",
        output_dir,
    )


