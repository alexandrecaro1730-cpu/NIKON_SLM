def plot_outlier_assessment(
    eda_output_dir: str | Path,
    output_dir: str | Path = BUSINESS_FIGURES_DIR,
    top_n: int = 12,
) -> None:
    """
    Business objective
    ------------------
    Show whether numeric features contain unusual values and interpret the
    absence of outliers from a manufacturing data perspective.

    Coding objective
    ----------------
    Read outliers.csv and plot the top features by IQR outlier percentage.
    """

    eda_output_dir = Path(eda_output_dir)
    output_dir = Path(output_dir)

    path = eda_output_dir / "outliers.csv"

    if not path.exists():
        return

    outliers = pd.read_csv(path)

    if outliers.empty or "outlier_percent" not in outliers.columns:
        return

    plot_df = outliers.copy()

    # Sample_ID is an identifier, not a physical process feature.
    plot_df = plot_df[plot_df["feature"] != "Sample_ID"]

    plot_df = plot_df.sort_values("outlier_percent", ascending=False).head(top_n)
    plot_df = plot_df.sort_values("outlier_percent", ascending=True)

    plt.figure(figsize=(11, 6))
    ax = sns.barplot(
        data=plot_df,
        x="outlier_percent",
        y="feature",
    )

    max_value = plot_df["outlier_percent"].max()

    if max_value == 0:
        ax.set_xlim(0, 1)
    else:
        ax.set_xlim(0, max_value * 1.2)

    ax.text(
        0.98,
        0.08,
        (
            "Engineering interpretation:\n"
            "No IQR outliers detected.\n\n"
            "This is unusual for raw production data\n"
            "and suggests controlled, pre-cleaned,\n"
            "or synthetic operating windows.\n\n"
            "No automatic outlier removal applied."
        ),
        transform=ax.transAxes,
        ha="right",
        va="bottom",
        fontsize=9,
        bbox=dict(boxstyle="round,pad=0.45", facecolor="white", edgecolor="black"),
    )

    plt.title("Outlier Assessment by Numeric Feature")
    plt.xlabel("IQR Outlier Share (%)")
    plt.ylabel("Feature")

    _save_plot(output_dir / "05_outlier_assessment.png")


