def plot_process_window_power_speed(
    df: pd.DataFrame,
    output_dir: str | Path = BUSINESS_FIGURES_DIR,
) -> None:
    """
    Business objective
    ------------------
    Show whether poor/moderate quality risk concentrates in specific LPBF
    power-speed operating windows without overplotting thousands of points.

    Coding objective
    ----------------
    Bin scan speed and laser power into a 2D process-window grid, calculate the
    Poor + Moderate rate in each bin, and visualize the result as a heatmap.
    """

    output_dir = Path(output_dir)

    required = {"Laser_Power_W", "Scan_Speed_mm_s", TARGET_COLUMN}

    if not required.issubset(df.columns):
        return

    risk_classes = {"Poor", "Moderate"}
    work = df[list(required)].dropna().copy()

    if work.empty:
        return

    work["scan_speed_bin"] = pd.cut(
        work["Scan_Speed_mm_s"],
        bins=6,
        include_lowest=True,
    )
    work["laser_power_bin"] = pd.cut(
        work["Laser_Power_W"],
        bins=6,
        include_lowest=True,
    )
    work["is_risk_class"] = work[TARGET_COLUMN].isin(risk_classes).astype(int)

    grouped = (
        work.groupby(["laser_power_bin", "scan_speed_bin"], observed=True)
        .agg(
            samples=(TARGET_COLUMN, "size"),
            risk_rate_percent=("is_risk_class", lambda x: x.mean() * 100),
        )
        .reset_index()
    )

    # Convert interval labels into short readable axis labels.
    grouped["scan_label"] = grouped["scan_speed_bin"].apply(
        lambda interval: f"{interval.left:.0f}-{interval.right:.0f}"
    )
    grouped["power_label"] = grouped["laser_power_bin"].apply(
        lambda interval: f"{interval.left:.0f}-{interval.right:.0f}"
    )

    heatmap_data = grouped.pivot(
        index="power_label",
        columns="scan_label",
        values="risk_rate_percent",
    )

    sample_data = grouped.pivot(
        index="power_label",
        columns="scan_label",
        values="samples",
    )

    # Preserve physical order: low power at bottom, high power at top.
    heatmap_data = heatmap_data.iloc[::-1]
    sample_data = sample_data.iloc[::-1]

    annotation = heatmap_data.copy().astype(object)
    for row_label in heatmap_data.index:
        for col_label in heatmap_data.columns:
            rate = heatmap_data.loc[row_label, col_label]
            count = sample_data.loc[row_label, col_label]
            annotation.loc[row_label, col_label] = (
                "" if pd.isna(rate) else f"{rate:.1f}%\nn={int(count)}"
            )

    fig, ax = plt.subplots(figsize=(12.5, 7.2))

    sns.heatmap(
        heatmap_data,
        annot=annotation,
        fmt="",
        cmap="YlOrRd",
        vmin=0,
        vmax=100,
        linewidths=0.5,
        linecolor="white",
        cbar_kws={"label": "Poor + Moderate rate (%)"},
        ax=ax,
    )

    ax.set_title("LPBF Process Window Risk: Laser Power vs Scan Speed", fontsize=14)
    ax.set_xlabel("Scan Speed Bin (mm/s)")
    ax.set_ylabel("Laser Power Bin (W)")
    ax.tick_params(axis="x", rotation=25)
    ax.tick_params(axis="y", rotation=0)

    ax.text(
        0.01,
        -0.18,
        (
            "Method: scan speed and laser power are binned into a 6×6 process "
            "window. Each cell shows Poor + Moderate rate and sample count. "
            "Interpretation: no strong high-risk pocket is visible; risk appears "
            "broadly distributed across the window."
        ),
        transform=ax.transAxes,
        ha="left",
        va="top",
        fontsize=9,
        wrap=True,
    )

    fig.subplots_adjust(bottom=0.24, top=0.88, left=0.12, right=0.96)
    _save_plot(output_dir / "07_process_window_power_speed.png")


def plot_energy_density_quality_physics(
    df: pd.DataFrame,
    output_dir: str | Path = BUSINESS_FIGURES_DIR,
) -> None:
    """
    Business objective
    ------------------
    Test whether energy-density regions show different quality risk without
    relying on unreadable point clouds.

    Coding objective
    ----------------
    Bin Energy_Density_J_mm3, calculate Poor + Moderate rate per bin, and show
    sample counts so reviewers can judge whether a visible trend is supported.
    """

    output_dir = Path(output_dir)

    required = {"Energy_Density_J_mm3", TARGET_COLUMN}

    if not required.issubset(df.columns):
        return

    risk_classes = {"Poor", "Moderate"}
    work = df[list(required)].dropna().copy()

    if work.empty:
        return

    work["energy_density_bin"] = pd.cut(
        work["Energy_Density_J_mm3"],
        bins=8,
        include_lowest=True,
    )
    work["is_risk_class"] = work[TARGET_COLUMN].isin(risk_classes).astype(int)

    summary = (
        work.groupby("energy_density_bin", observed=True)
        .agg(
            samples=(TARGET_COLUMN, "size"),
            risk_rate_percent=("is_risk_class", lambda x: x.mean() * 100),
        )
        .reset_index()
    )

    summary["energy_label"] = summary["energy_density_bin"].apply(
        lambda interval: f"{interval.left:.0f}-{interval.right:.0f}"
    )

    overall_risk_rate = work["is_risk_class"].mean() * 100

    fig, ax = plt.subplots(figsize=(12.5, 6.8))

    sns.barplot(
        data=summary,
        x="energy_label",
        y="risk_rate_percent",
        ax=ax,
    )

    ax.axhline(
        overall_risk_rate,
        linestyle="--",
        linewidth=1.4,
        color="black",
        label=f"Overall Poor + Moderate rate: {overall_risk_rate:.1f}%",
    )

    for index, row in summary.iterrows():
        ax.text(
            index,
            row["risk_rate_percent"] + 0.8,
            f"{row['risk_rate_percent']:.1f}%\nn={int(row['samples'])}",
            ha="center",
            va="bottom",
            fontsize=8,
        )

    ax.set_ylim(0, max(60, summary["risk_rate_percent"].max() + 10))
    ax.set_title("Energy Density Quality Risk by Operating Bin", fontsize=14)
    ax.set_xlabel("Energy Density Bin (J/mm³)")
    ax.set_ylabel("Poor + Moderate Rate (%)")
    ax.tick_params(axis="x", rotation=25)
    ax.legend(loc="upper right")

    ax.text(
        0.01,
        -0.24,
        (
            "Method: energy density is binned into 8 operating ranges. Each bar "
            "shows the share of Poor + Moderate samples in that range. LPBF "
            "expectation: very low energy density can cause lack of fusion; very "
            "high energy density can cause instability/keyholing. Observation: "
            "risk stays close to the overall baseline, so this dataset does not "
            "show a strong energy-density quality trend."
        ),
        transform=ax.transAxes,
        ha="left",
        va="top",
        fontsize=9,
        wrap=True,
    )

    fig.subplots_adjust(bottom=0.30, top=0.88, left=0.10, right=0.96)
    _save_plot(output_dir / "08_energy_density_quality_physics.png")


