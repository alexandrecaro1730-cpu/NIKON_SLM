def plot_actionable_categorical_summary(
    df: pd.DataFrame,
    output_dir: str | Path = BUSINESS_FIGURES_DIR,
) -> None:
    """
    Business objective
    ------------------
    Summarize whether early business-actionable categorical choices have strong
    differences in quality risk.

    Coding objective
    ----------------
    Compute Poor + Moderate percentage for each categorical feature value and
    show the result as a concise table.
    """

    output_dir = Path(output_dir)

    risk_classes = {"Poor", "Moderate"}
    rows = []

    for feature in CATEGORICAL_FEATURES:
        if feature not in df.columns:
            continue

        grouped = df.groupby(feature)[TARGET_COLUMN]

        for category, values in grouped:
            count = len(values)
            risk_count = int(values.isin(risk_classes).sum())
            risk_rate = risk_count / count * 100 if count > 0 else 0

            rows.append(
                {
                    "Variable": feature,
                    "Category": str(category),
                    "Poor + Moderate Rate": f"{risk_rate:.1f}%",
                    "Samples": count,
                }
            )

    risk_df = pd.DataFrame(rows)

    if risk_df.empty:
        return

    risk_df["_risk_numeric"] = (
        risk_df["Poor + Moderate Rate"].str.replace("%", "").astype(float)
    )
    risk_df = risk_df.sort_values("_risk_numeric", ascending=False).drop(
        columns="_risk_numeric"
    )

    fig, ax = plt.subplots(figsize=(13, 5.8))
    ax.axis("off")

    ax.text(
        0,
        1.08,
        "Actionable Categorical Variables",
        fontsize=16,
        fontweight="bold",
        ha="left",
        transform=ax.transAxes,
    )

    ax.text(
        0,
        1.0,
        (
            "These variables are available before production decisions. "
            "Observed risk differences are modest, suggesting process variables "
            "may carry more signal."
        ),
        fontsize=10,
        ha="left",
        transform=ax.transAxes,
    )

    table = ax.table(
        cellText=risk_df[
            ["Variable", "Category", "Poor + Moderate Rate", "Samples"]
        ].values,
        colLabels=["Variable", "Category", "Poor + Moderate Rate", "Samples"],
        loc="center",
        cellLoc="left",
        colLoc="left",
    )

    table.auto_set_font_size(False)
    table.set_fontsize(8.8)
    table.scale(1, 1.45)

    for key, cell in table.get_celld().items():
        cell.set_edgecolor("black")
        if key[0] == 0:
            cell.set_text_props(weight="bold")

    ax.text(
        0,
        -0.08,
        (
            "Conclusion: categorical planning variables alone are unlikely to "
            "explain quality; process and monitoring features are expected to "
            "be more useful."
        ),
        fontsize=10,
        fontweight="bold",
        ha="left",
        transform=ax.transAxes,
    )

    _save_plot(output_dir / "13_actionable_categorical_summary.png")


